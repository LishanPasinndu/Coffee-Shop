    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String price = jTextField2.getText();
        String category = foodcomobobox.getSelectedItem().toString();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Product Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Unit price of product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT * FROM `category` WHERE `name`='" + category + "'");
                rs1.next();

                String cid = rs1.getString("id");

                ResultSet rs = MySQL.search("SELECT * FROM `food` WHERE `name`='" + name + "' AND `price`='" + price + "' AND `category_id`='" + cid + "' ");

                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "this " + category + " already registerd", "Success", JOptionPane.INFORMATION_MESSAGE);

                } else {

                    MySQL.iud("INSERT INTO `food`(`name`,`price`,`category_id`) VALUES('" + name + "','" + price + "','" + cid + "')  ");

                    JOptionPane.showMessageDialog(this, "this" + category + " is Registerd ", "Success", JOptionPane.INFORMATION_MESSAGE);

                    loadfood();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    foodcomobobox.setSelectedItem("Select");

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton6ActionPerformed

